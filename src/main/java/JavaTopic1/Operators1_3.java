package JavaTopic1;

public class Operators1_3 {
    //Exercise 1.3. Using the following operators ("+","-","*" and "/"),
    //try to do an example with each of them. Try to use different data types.

    public static void main(String[] args) {
            int x = 4;
            int y = 2;

            System.out.println(x + y);

            System.out.println(x / 2);

            System.out.println(x - 2);

            System.out.println(x * 2);

    }



}
